/*
 * Print parimeter and area where radius is 5.5
 */
package chapter1;

public class Question1_8 {
    
    public static void main(String[] args) {
        double radius = 5.5;
        System.out.println("perimeter: " + (2 * radius * Math.PI));
        System.out.println("area: " + (radius * radius * Math.PI));
    }
    
}
