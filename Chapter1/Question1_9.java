/*
 * Display Area and Perimeter of a rectangle:
 * Width 4.5 height 7.9
 */
package chapter1;


public class Question1_9 {
    
    public static void main(String[] args) {
        double width = 4.5;
        double height = 7.9;
        System.out.println("Area: " + (width + height));
        System.out.println("Perimeter: " + (width + height + width + height));
    }
    
}
